const int N = 15;
const int COLORS = 5;
int board[N * N];
std::vector<std::pair<int, int>> result;

bool equal(int b1[N * N], int b2[N * N]) {
    int perm[COLORS];
    std::fill(perm, perm + COLORS, -1);

    for (int i = 0; i < N * N; ++i) {
        if (b1[i] == -1 || b2[i] == -1) {
            if (b1[i] != b2[i])
                return false;
            continue;
        }
        if (perm[b1[i]] == -1)
            perm[b1[i]] = b2[i];
        if (perm[b1[i]] != b2[i])
            return false;
    }
    return true;
}

